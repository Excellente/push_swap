#include "libft.h"

int	ft_toupper(int c)
{
	if (!(ft_isalpha(c)))
	{
		return (0);
	}
	else if (c >= 65 && c <= 90)
	{
		return (c);
	}
	else
	{
		c = 90 - (122 - c);
		return (c);
	}
}
