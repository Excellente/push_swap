#include "libft.h"

int	ft_tolower(int c)
{
	if (!(ft_isalpha(c)))
	{
		return (0);
	}
	else if (c >= 97 && c <= 122)
	{
		return (c);
	}
	else
	{
		c = 122 - (90 - c);
		return (c);
	}
}
